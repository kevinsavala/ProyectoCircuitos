    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        double x, y;
        int finalx, finaly;
        if(tipo.getSelectedIndex()==0){
            x = Double.parseDouble(textA.getText());
            y = Double.parseDouble(textB.getText());
            finalx = (int)Math.round(x);
            finaly = (int)Math.round(y);
            Main.main(null,finalx,finaly);
            this.dispose();
        } else if(tipo.getSelectedIndex()==1){
            x = Double.parseDouble(textA.getText());
            y = Double.parseDouble(textB.getText());
            Complejo nuevo = Complejo.convertToBinomio(x, y);
            finaly = (int)Math.round(nuevo.getA());
            finalx = (int)Math.round(nuevo.getB());
            Main.main(null,finalx,finaly);
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

